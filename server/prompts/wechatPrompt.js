function getWechatPrompt(topic, count, compactText) {
  const safeTopic = compactText(topic) || "这个话题";

  return `
请围绕主题“${safeTopic}”，为微信公众号生成 ${count} 个标题候选。

【风格要求（非常重要）】

整体风格参考高阅读量观点号（类似“也评”）：

1. 以“判断”为核心，而不是描述或总结
2. 语气克制，不夸张，但有明显态度
3. 不把话说透，要有“留一口气”的感觉
4. 像一个见过事的人在提醒，而不是在教学或分析
5. 标题要让人产生一种感觉：
   👉 “这件事可能没那么简单”
   👉 “我可能忽略了什么”

---

【表达倾向】

可以接近这些表达方式（但不要重复套模板）：

- 很多人都在聊X，但真正的问题可能不在这里  
- 这件事最值得警惕的，未必是表面  
- 说实话，看到X，我有点不太安心  
- 别只盯着X，后面的变化可能更关键  
- X这件事，可能没那么简单  
- 真正需要在意的，其实还没被说出来  

---

【必须避免】

- “关于XX的分析 / 几点思考”  
- 明显的标题党（震惊、必看）  
- 过度完整表达（不要把逻辑讲完）  
- 每个标题结构一样  

---

【长度】

- 控制在 16~24 字之间
- 可以略微不完整（更像人写的）

---

【输出格式】

返回 JSON：
{
  "titles": [
    { "title": "标题1", "score": 95, "reason": "为什么这个标题更容易让人点开（从判断/留白角度说明）" }
  ],
  "bestTitle": "最佳标题"
}

只返回 JSON，不要解释
`.trim();
}
module.exports = {
  getWechatPrompt,
};