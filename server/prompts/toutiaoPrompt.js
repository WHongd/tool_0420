function getToutiaoPrompt(topic, count, compactText) {
  const safeTopic = compactText(topic) || "这个话题";

  return `
请围绕主题“${safeTopic}”，为今日头条生成 ${count} 个标题候选。

【核心目标】
在3秒内抓住用户，让人产生：
👉 这事不简单
👉 我可能看错了
👉 这和我有关

---

【必须包含（至少2个）】

1. 冲突 / 反转  
- 很多人骂错了  
- 问题不在表面  

2. 判断 / 立场  
- 明确观点  
- 可以不讨好  

3. 利益 / 影响  
- 普通人会不会吃亏  
- 谁会受影响  

4. 结果 / 后果  
- 接下来会发生什么  
- 会带来什么变化  

---

【推荐句式】

✔ 很多人都在骂X，但问题根本不在这  
✔ 这件事最危险的，不是表面  
✔ X背后，普通人可能才是最吃亏的  
✔ 说句不好听的，很多人根本没看懂  
✔ 这件事你站哪边，其实很关键  
✔ 别只盯着X，真正的变化在后面  

---

【风格要求】

- 更短（12~20字）  
- 更直接  
- 更有冲击  

---

【禁止】

- 分析型标题  
- 标题党  
- 全部同结构  

---

【输出格式】

返回 JSON：
{
  "titles": [
    { "title": "标题1", "score": 95, "reason": "点击原因（冲突/利益/判断）" }
  ],
  "bestTitle": "最佳标题"
}

只返回 JSON，不要解释
`.trim();
}

module.exports = {
  getToutiaoPrompt,
};